pub fn encode_huffman(text: &str) -> Option<Vec<u8>> {
    let mut bits = String::new();
    for ch in text.chars() {
        let value = ch.to_string();
        bits.push_str(CODES.iter().find(|(_, c)| *c == value)?.0);
    }
    let mut out = Vec::with_capacity((bits.len() + 7) / 8);
    for chunk in bits.as_bytes().chunks(8) {
        let mut byte = 0_u8;
        for (i, bit) in chunk.iter().enumerate() {
            if *bit == b'1' {
                byte |= 1 << (7 - i);
            }
        }
        out.push(byte);
    }
    Some(out)
}

pub fn decode_huffman(bytes: &[u8]) -> String {
    let mut result = String::new();
    let mut buffer = String::new();
    for byte in bytes {
        for shift in (0..8).rev() {
            buffer.push(if byte & (1 << shift) != 0 { '1' } else { '0' });
            if let Some((_, value)) = CODES.iter().find(|(code, _)| *code == buffer) {
                result.push_str(value);
                buffer.clear();
            }
        }
    }
    result
}

/// Decodes exactly `expected_chars` and accepts only zero-bit byte padding.
pub fn decode_huffman_exact(bytes: &[u8], expected_chars: usize) -> Option<String> {
    if expected_chars == 0 {
        return bytes.is_empty().then(String::new);
    }
    let total_bits = bytes.len().checked_mul(8)?;
    let mut result = String::new();
    let mut buffer = String::new();
    let mut produced = 0;
    for position in 0..total_bits {
        let bit = (bytes[position / 8] >> (7 - position % 8)) & 1;
        buffer.push(if bit == 1 { '1' } else { '0' });
        if let Some((_, value)) = CODES.iter().find(|(code, _)| *code == buffer) {
            result.push_str(value);
            buffer.clear();
            produced += 1;
            if produced == expected_chars {
                let remaining = total_bits - position - 1;
                if remaining > 7
                    || (position + 1..total_bits).any(|p| bytes[p / 8] & (1 << (7 - p % 8)) != 0)
                {
                    return None;
                }
                return Some(result);
            }
        } else if !CODES.iter().any(|(code, _)| code.starts_with(&buffer)) {
            return None;
        }
    }
    None
}
